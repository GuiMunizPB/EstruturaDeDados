class NoArvore {
    constructor() {
        this.conteudo = 0;
        this.esq = null;
        this.dir = null;
    }

    getConteudo() {
        return this.conteudo;
    }

    setConteudo(conteudo) {
        this.conteudo = conteudo;
    }

    getEsq() {
        return this.esq;
    }

    setEsq(esq) {
        this.esq = esq;
    }

    getDir() {
        return this.dir;
    }

    setDir(dir) {
        this.dir = dir;
    }
}
